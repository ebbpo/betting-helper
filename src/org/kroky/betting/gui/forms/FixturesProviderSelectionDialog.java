    private void btnNewFixturesProviderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewFixturesProviderActionPerformed
        NewProviderDialog d = new NewProviderDialog(this, true, ProviderType.FIXTURES, team);
        d.setVisible(true);
        final Provider p = d.getProvider();
        if(p != null) {
            cbExistingProviders.insertItemAt(p, 0);
            cbExistingProviders.setSelectedItem(p);
            btnOkActionPerformed(evt);
        }
    }//GEN-LAST:event_btnNewFixturesProviderActionPerformed


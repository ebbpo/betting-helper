    private void initData() {
        List<Provider> list = new ArrayList<Provider>();
        list.addAll(DAO.getFixtureProviders(team));
        cbExistingProviders.setModel(new SortedComboBoxModel(list.toArray(new Provider[list.size()])));
        if (team.getFixturesProvider() != null) {
            cbExistingProviders.setSelectedItem(team.getFixturesProvider());
        }
        pack();
        Utils.centerOnParent(owner, this);
    }
